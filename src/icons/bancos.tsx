/** Chaves válidas — o que `contas.icone` / `cartoes.icone` guardam (§4.9). */
export type ChaveBanco =
  | 'santander'
  | 'itau'
  | 'nu'
  | 'master'
  | 'bv'
  | 'caixa'
  | 'sams'
  | 'visa';

/** Mapa chave → componente. Use para resolver `icone` em runtime. */
export const BANCOS: Record<ChaveBanco, (p: LogoProps) => JSX.Element> = {
  santander: LogoSantander,
  itau: LogoItau,
  nu: LogoNu,
  master: LogoMaster,
  bv: LogoBv,
  caixa: LogoCaixa,
  sams: LogoSams,
  visa: LogoVisa,
};

/**
 * Resolve uma chave de banco num logo. Chave ausente/desconhecida → null
 * (o chamador decide o fallback: inicial, placeholder neutro, etc.).
 */
export function LogoBanco({
  chave,
  tamanho = 28,
}: {
  chave?: string | null;
  tamanho?: number;
}) {
  if (!chave) return null;
  const Comp = BANCOS[chave as ChaveBanco];
  return Comp ? <Comp tamanho={tamanho} /> : null;
}